    private void LoadEmpleado(EmpleadoModel e){
        cedulaEmpleadoLbl.setText(e.getCedula());
        nombreEmpleadoTxt.setText(e.getNombre());
        int selectedIndex = cargoEmpleadoCbx.getItemCount() - 1;
        for(int i=0; i < cargoEmpleadoCbx.getItemCount(); i++){
            String item = cargoEmpleadoCbx.getItemAt(i);
            if(item.equals(e.getCargo())){
                selectedIndex = i;
                break;
            }
        }
        cargoEmpleadoCbx.setSelectedIndex(selectedIndex);
        correoEmpleadoTxt.setText(e.getCorreo());
        celularEmpleadoTxt.setText(e.getCelular());
        contraseñaEmpleadoTxt.setText(e.getContraseña());
    }
     
    private void LoadEmpleados(JTable empleadosTbl){                       
        DefaultTableModel tabla = (DefaultTableModel) empleadosTbl.getModel();       
        tabla.setRowCount(0);
        empleadoController.findAll().forEach((u) -> tabla.addRow(new Object[] {u.getId(), u.getCedula(), u.getNombre(), u.getCargo(), u.getCorreo(), u.getCelular(), u.getContraseña()}));        
    }
    
