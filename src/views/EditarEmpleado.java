    private void editarEmpleadoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarEmpleadoBtnActionPerformed
        if(nombreEmpleadoTxt.getText().equals("") || cargoEmpleadoCbx.getSelectedItem().toString().equals("") || correoEmpleadoTxt.getText().equals("") || contraseñaEmpleadoTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Complete Los Campos Necesarios");
        }else{
            empleado = new EmpleadoModel(cedulaEmpleadoLbl.getText(), nombreEmpleadoTxt.getText(), cargoEmpleadoCbx.getSelectedItem().toString(), correoEmpleadoTxt.getText(), celularEmpleadoTxt.getText(), contraseñaEmpleadoTxt.getText());
            if(empleadoController.update(empleado)){                        
                JOptionPane.showMessageDialog(null, "Empleado Editado Correctamente:\n" + this.empleado.getNombre());
                LoadEmpleados(this.empleadosTabla);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "El Empleado No Pudo Ser Editado");
                this.dispose();
            }            
        }        
    }//GEN-LAST:event_editarEmpleadoBtnActionPerformed
        
        

