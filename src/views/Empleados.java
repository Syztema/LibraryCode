    private void crearEmpleadoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearEmpleadoBtnActionPerformed
        //VALIDACION DE NULOS
        if(cedulaEmpleadoTxt.getText().equals("") || nombreEmpleadoTxt.getText().equals("") || cargoEmpleadoCbx.getSelectedItem().toString().equals("") || correoEmpleadoTxt.getText().equals("") || contrase単aEmpleadoTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Complete Los Campos Necesarios");
        }else{
            //VALIDACION DE EXISTENCIA DE EMPLEADO
            this.empleado = (EmpleadoModel) empleadoController.findByCedula(cedulaEmpleadoTxt.getText());
            if(this.empleado != null){
                JOptionPane.showMessageDialog(null, "Ya existe un empleado con la cedula " + cedulaEmpleadoTxt.getText() + " \nVerifique Nuevamente");
                this.empleado = null;
            }else{
                this.empleado = new EmpleadoModel(cedulaEmpleadoTxt.getText(), nombreEmpleadoTxt.getText(), cargoEmpleadoCbx.getSelectedItem().toString(), correoEmpleadoTxt.getText(), celularEmpleadoTxt.getText(), contrase単aEmpleadoTxt.getText());
                if(empleadoController.create(empleado)){
                    
                    JOptionPane.showMessageDialog(null, "Empleado Agregado Exitosamente: \n"  + nombreEmpleadoTxt.getText());
                    
                    cedulaEmpleadoTxt.setText("");
                    nombreEmpleadoTxt.setText("");
                    cargoEmpleadoCbx.setSelectedIndex(0);
                    correoEmpleadoTxt.setText("");
                    celularEmpleadoTxt.setText("");
                    contrase単aEmpleadoTxt.setText("");
                                        
                    
                    LoadEmpleados();
                    this.empleado = null;
                }else{
                    JOptionPane.showMessageDialog(null, "Empleado NO guardado, intente de nuevo: \n" + nombreEmpleadoTxt.getText());
                    this.empleado = null;
                }                    
            }
        }
        
    }//GEN-LAST:event_crearEmpleadoBtnActionPerformed

