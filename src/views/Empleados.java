    private void eliminarEmpleadoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarEmpleadoBtnActionPerformed
        // TODO add your handling code here:
        if(empleado == null){
            JOptionPane.showMessageDialog(null, "Consulte el Empleado a Eliminar");
        }else{
            int confirmacion = JOptionPane.showConfirmDialog(null, "Desea eliminar el Empleado:\n" + empleado.getNombre());
            if(confirmacion == JOptionPane.YES_OPTION){
                if(empleadoController.deleteByCedula(empleado.getCedula())){
                    JOptionPane.showMessageDialog(null, "Empleado Eliminado Correctamente");                       
                }
                cedulaEmpleadoConsultaTxt.setText("");
                nombreLbl.setText("");
                cargoLbl.setText("");
                correoLbl.setText("");
                celularLbl.setText("");
                contrase単aLbl.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "El Empleado No Ha Sido Eliminado");                       
            }
        }
        LoadEmpleados();
    }//GEN-LAST:event_eliminarEmpleadoBtnActionPerformed

