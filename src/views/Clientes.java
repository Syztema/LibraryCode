    private void crearClienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearClienteBtnActionPerformed
        //VALIDACION DE NULOS
        if(cedulaClienteTxt.getText().equals("") || nombreClienteTxt.getText().equals("") || membresiaClienteCbx.getSelectedItem().toString().equals("") || correoClienteTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Complete Los Campos Necesarios");
        }else{
            //VALIDACION EXISTENCIA DE CLIENTE
            this.cliente = (ClienteModel) clienteController.findByCedula(cedulaClienteTxt.getText());
            if(this.cliente != null){
                JOptionPane.showMessageDialog(null, "Ya existe un cliente con la cedula " + cedulaClienteTxt.getText() + " \nVerifique Nuevamente");
                this.cliente = null;
            }else{
                this.cliente = new ClienteModel(cedulaClienteTxt.getText(), nombreClienteTxt.getText(), membresiaClienteCbx.getSelectedItem().toString(), correoClienteTxt.getText(), celularClienteTxt.getText());
                if(clienteController.create(this.cliente)){
                    
                    JOptionPane.showMessageDialog(null, "Cliente Agregado Exitosamente: \n"  + nombreClienteTxt.getText());
                    
                    cedulaClienteTxt.setText("");
                    nombreClienteTxt.setText("");
                    membresiaClienteCbx.setSelectedIndex(0);
                    correoClienteTxt.setText("");
                    celularClienteTxt.setText("");
                    
                    LoadClientes();
                    this.cliente = null;
                }else{
                    JOptionPane.showMessageDialog(null, "Cliente NO guardado, intente de nuevo: \n" + nombreClienteTxt.getText());
                    this.cliente = null;
                }
            }
        }
    }//GEN-LAST:event_crearClienteBtnActionPerformed

    private void consultarClienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarClienteBtnActionPerformed
        // TODO add your handling code here:
        cliente = (ClienteModel) clienteController.findByCedula(cedulaClienteConsultaTxt.getText());
        if(cliente != null){
            nombreLbl.setText(cliente.getNombre());
            membresiaLbl.setText(cliente.getMembresia());
            correoLbl.setText(cliente.getCorreo());
            celularLbl.setText(cliente.getCelular());
        }else{
            JOptionPane.showMessageDialog(null, "Cliente No Existente");
            cedulaClienteConsultaTxt.setText("");
            nombreLbl.setText("");
            membresiaLbl.setText("");
            correoLbl.setText("");
            celularLbl.setText("");            
        }
    }//GEN-LAST:event_consultarClienteBtnActionPerformed

