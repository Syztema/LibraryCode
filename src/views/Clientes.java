    private void eliminarClienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarClienteBtnActionPerformed
        // TODO add your handling code here:
        if(cliente == null){
            JOptionPane.showMessageDialog(null, "Consulte el Cliente a Eliminar");
        }else{
            int confirmacion = JOptionPane.showConfirmDialog(null, "Desea eliminar el Cliente:\n" + cliente.getNombre());
            if(confirmacion == JOptionPane.YES_OPTION){
                if(clienteController.deleteByCedula(cliente.getCedula())){
                    JOptionPane.showMessageDialog(null, "Cliente Eliminado Correctamente");                       
                }
                cedulaClienteConsultaTxt.setText("");
                nombreLbl.setText("");
                membresiaLbl.setText("");
                correoLbl.setText("");
                celularLbl.setText("");    
            }else{
                JOptionPane.showMessageDialog(null, "El Cliente No Ha Sido Eliminado");                       
            }
        }
        LoadClientes();
    }//GEN-LAST:event_eliminarClienteBtnActionPerformed
    
    private void setJTexFieldChanged(JTextField txt){
        DocumentListener documentListener = new DocumentListener() {
 
        @Override
        public void changedUpdate(DocumentEvent documentEvent) {
            printIt(documentEvent);
        }
 
        @Override
        public void insertUpdate(DocumentEvent documentEvent) {
            printIt(documentEvent);
        }
 
        @Override
        public void removeUpdate(DocumentEvent documentEvent) {
            printIt(documentEvent);
        }
        };
        txt.getDocument().addDocumentListener(documentListener);
 
    } 
    private void printIt(DocumentEvent documentEvent) {
        DocumentEvent.EventType type = documentEvent.getType();
 
        if (type.equals(DocumentEvent.EventType.CHANGE))
        {
            
        }
        else if (type.equals(DocumentEvent.EventType.INSERT))
        {
           //Método a ejecutar            
        }
        else if (type.equals(DocumentEvent.EventType.REMOVE))
        {
            //Método a ejecutar
            nombreLbl.setText("");
            membresiaLbl.setText("");
            correoLbl.setText("");
            celularLbl.setText("");             
            this.cliente = null;
        }
    }
    
